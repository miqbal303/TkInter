# Import the Tkinter library and alias it as 'tk'.
import tkinter as tk

# Create the main application window.
root = tk.Tk()

# Set the title of the application window.
root.title("My first Tkinter Application")

# Set the minimum size (width and height) for the application window.
root.minsize(300, 200)

# Start the Tkinter main event loop, which keeps the window open and responsive.
root.mainloop()
